# Using ord get the ASCII value of alphabets
# ord : convert character in integer
ch=input("Enter Character: ")
# ch='A'
print("The ASCII value of",ch ,"is",ord(ch))


# Using chr get the alphabets of decimal number
# chr : convert integer in character
num=int(input("ENter num: "))
# num=65
print("The character at ASCII value",num,"is",chr(num))
